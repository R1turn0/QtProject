// 获取设备序列号
int getUserDevSN(const char *devp_id, char* deviceSN)
{
	int ret	= SS_OK;

    int i = 0;
    int dev_index = 0;

    void *ipc = NULL;

    cJSON *item = NULL;
    cJSON *device = NULL;
    cJSON *dev_desc_array = NULL;

    char *dev_desc_array_str = NULL;
    int dev_desc_array_size = 0;

    char *dev_desc_str = NULL;

    ret = slm_ctrl_client_open(&ipc);
    if (ret != SS_OK)
    {
        goto l_end;
    }

    // 枚举本地插入的所有加密锁（会返回所有开发者ID的精锐5加密锁）
    ret = slm_ctrl_get_local_description(ipc, JSON, &dev_desc_array_str);
    if (ret != SS_OK)
    {
        printf("slm_ctrl_get_local_description error : 0x%08X\n", ret);
        goto l_end;
    }

    dev_desc_array = cJSON_Parse(dev_desc_array_str);
    dev_desc_array_size = cJSON_GetArraySize(dev_desc_array);

    printf("slm_ctrl_get_local_description found dongle count: %d\n", dev_desc_array_size);
    if (dev_desc_array_size == 0)
    {
        goto l_end;
    }

    // 根据开发者ID过滤无效的加密锁
    for (i = 0; i < dev_desc_array_size; i++)
    {
        device = cJSON_GetArrayItem(dev_desc_array, i);
        item = cJSON_GetObjectItem(device, "developer_id");
        assert(item && item->valuestring && strlen(item->valuestring) > 0);

		if (strcmp(devp_id, item->valuestring) == 0)
        {
            if (dev_index == FIND_DEVICE_INDEX)        // 获取查找到的第一把加密锁
            {
                break;
            }

            dev_index++;
        }
    }

    // 确认是否找到指定开发者ID的加密锁
    if (dev_index < FIND_DEVICE_INDEX)
    {
        printf("slm_ctrl_get_local_description not found developer_id = %s index = %d\n", devp_id, FIND_DEVICE_INDEX);   
        goto l_end;
    }

    dev_desc_str = cJSON_PrintUnformatted(device);
    if (dev_desc_str == NULL)
    {
        printf("cJSON_PrintUnformatted failed\n");
        goto l_end;
    }
    printf("first device desc: %s\n", dev_desc_str);

    item = cJSON_GetObjectItem(device, "sn");
    if (item == NULL || strlen(item->valuestring) == 0)
    {
        printf("device description not found SN item.\n");
        goto l_end;
    }

    memcpy(deviceSN, item->valuestring, strlen(item->valuestring));

l_end:
	if(ipc)
    {
	    slm_ctrl_client_close(ipc);	
    }

    if (dev_desc_array_str)
    {
        slm_ctrl_free(dev_desc_array_str);
    }

    if (dev_desc_array)
    {
        cJSON_Delete(dev_desc_array);
    }

    if (dev_desc_str)
    {
        free(dev_desc_str);
    }

	return ret;
}

// 获取指定索引的加密锁设备描述
char *getDeviceDescByIndex(void *ipc, char *developer_id, int index)
{
    int ret = SS_OK;
    int i = 0;
    int dev_index = 0;

    cJSON *device = NULL;
    cJSON *item = NULL;
    cJSON *dev_desc_array = NULL;

    char *dev_desc_array_str = NULL;
    int dev_desc_array_size = 0;

    char *dev_desc_str = NULL;

    assert(ipc);
    assert(index >= 0);

    // 枚举本地插入的所有加密锁（会返回所有开发者ID的精锐5加密锁）
    ret = slm_ctrl_get_local_description(ipc, JSON, &dev_desc_array_str);
    if (ret != SS_OK)
    {
        printf("slm_ctrl_get_local_description error : 0x%08X\n", ret);
        goto l_end;
    }

    dev_desc_array = cJSON_Parse(dev_desc_array_str);
    dev_desc_array_size = cJSON_GetArraySize(dev_desc_array);

    printf("slm_ctrl_get_local_description found dongle count: %d\n", dev_desc_array_size);
    if (dev_desc_array_size == 0)
    {
        goto l_end;
    }

    // 根据开发者ID过滤无效的加密锁
    for (i = 0; i < dev_desc_array_size; i++)
    {
        device = cJSON_GetArrayItem(dev_desc_array, i);
        item = cJSON_GetObjectItem(device, "developer_id");
        assert(item && item->valuestring && strlen(item->valuestring) > 0);

        if (strcmp(developer_id, item->valuestring) == 0)
        {
            if (dev_index == index)
            {
                break;
            }

            dev_index++;
        }
    }

    // 确认是否找到指定开发者ID的加密锁
    if (dev_index < index)
    {
        printf("slm_ctrl_get_local_description not found developer_id = %s index = %d\n", developer_id, index);   
        goto l_end;
    }

    dev_desc_str = cJSON_PrintUnformatted(device);
    if (dev_desc_str == NULL)
    {
        printf("cJSON_PrintUnformatted failed\n");
        goto l_end;
    }

l_end:
    if (dev_desc_array_str)
    {
        slm_ctrl_free(dev_desc_array_str);
    }

    if (dev_desc_array)
    {
        cJSON_Delete(dev_desc_array);
    }

    return dev_desc_str;
}

// 获取当前加密锁的设备证书链，兼容新旧版本的加密锁
int getUserDevP7b(const char *devp_id, unsigned char *pCertP7b, unsigned int *pCertLen)
{
	int ret = SS_OK;

    void *ipc = NULL;

    char *dev_desc_str = NULL;

	cert_st pcerts_st[3] = {0};

    ret = slm_ctrl_client_open(&ipc);
    if (ret != SS_OK)
    {
        printf("slm_ctrl_client_open error : 0x%08X\n", ret);
        goto l_end;
    }

    // TODO 开发者使用前需要修改 FIND_DEVICE_DEVELOPER_ID 内容，否则无法找到加密锁
    dev_desc_str = getDeviceDescByIndex(ipc, devp_id, FIND_DEVICE_INDEX);  // 获取查找到的第一把加密锁
    if (dev_desc_str == NULL)
    {
        printf("getDeviceDescByIndex not found dongle by index %d.\n", FIND_DEVICE_INDEX);
        ret = SS_ERROR_NO_LOCK;
        goto l_end;
    }

    // 获取根证书
    pcerts_st[0].certlen = x509_CERT_SIZE;
    ret = slm_ctrl_get_cert(ipc, dev_desc_str, CERT_TYPE_ROOT_CA, pcerts_st[0].certbuf,  pcerts_st[0].certlen, &pcerts_st[0].certlen);
    if (ret == SS_OK)
    {
        // 获取设备CA证书
        pcerts_st[1].certlen = x509_CERT_SIZE;
        ret = slm_ctrl_get_cert(ipc, dev_desc_str, CERT_TYPE_DEVICE_CA, pcerts_st[1].certbuf,  pcerts_st[1].certlen, &pcerts_st[1].certlen);
        if (ret != SS_OK)
        {
            printf("slm_ctrl_get_cert(CERT_TYPE_DEVICE_CA) error : 0x%08X\n", ret);
            goto l_end;
        }
    }
    else if (ret == SS_ERROR_LM_INVALID_PARAMETER)
    {
        // Virbox 用户工具版本过低，请登录深思官网下载最新版本的 Virbox 用户工具，安装后重试。
    }
    else if (ret == SS_ERROR_INNER_FILE_NOT_FOUND)
    {
        // 加密锁固件版本过低，不支持通过接口获取证书，使用固定的根证书合成设备证书链
        // 如果开发者购买加密锁生产日期在 2019 年后，则不需要此部分的兼容旧版本加密锁的代码。

        // 获取根证书（旧）
        pcerts_st[0].certlen = sizeof(g_formal_root_ca);
        memcpy(pcerts_st[0].certbuf, g_formal_root_ca, sizeof(g_formal_root_ca));

        // 获取设备CA证书（旧）
        pcerts_st[1].certlen = sizeof(g_formal_device_ca);
        memcpy(pcerts_st[1].certbuf, g_formal_device_ca, sizeof(g_formal_device_ca));
    }
    else
    {
        printf("slm_ctrl_get_cert(CERT_TYPE_ROOT_CA) error : 0x%08X\n", ret);
        goto l_end;
    }

    // 获取设备证书
    pcerts_st[2].certlen = x509_CERT_SIZE;
    ret = slm_ctrl_get_cert(ipc, dev_desc_str, CERT_TYPE_DEVICE_CERT, pcerts_st[2].certbuf,  pcerts_st[2].certlen, &pcerts_st[2].certlen);
    if (ret != SS_OK)
    {
        printf("slm_ctrl_get_cert(CERT_TYPE_DEVICE_CERT) error : 0x%08X\n", ret);
        goto l_end;
    }

	// 合成设备证书链（p7b）
	ret = ss_make_p7b(pcerts_st, sizeof(pcerts_st) / sizeof(cert_st), pCertP7b, pCertLen);
	if (ret)
	{
		printf("ss_make_p7b error :0x%08X\n", ret);
		return 1;
	}

    // 验证合成证书链是否正确
    ss_x509_init();
    ret = ss_verify_p7b(pCertP7b, (int)*pCertLen);
    ss_x509_cleanup();
    if (ret != 0)
    {
        printf("ss_verify_p7b error :0x%08X\n", ret);
    }

l_end:
    if (dev_desc_str)
    {
        free(dev_desc_str);
    }

    if (ipc)
    {
        slm_ctrl_client_close(ipc);
    }

	return ret;
}

static int masterEnablePIN(MASTER_HANDLE hD2CHandle)
{
#define MASTER_GET_INFO_TYPE_DEFAULT 1
    int ret = SS_OK;
    char *info_str_json = NULL;

    cJSON *root = NULL;
    cJSON *node_support_pin = NULL;
    cJSON *node_enable_pin = NULL;

    ret = master_get_info(hD2CHandle, MASTER_GET_INFO_TYPE_DEFAULT, &info_str_json);
    if (ret != SS_OK)
    {
        return 0;
    }

    root = cJSON_Parse(info_str_json);
    if (root == NULL)
    {
        ret = 0;
        goto END;
    }

    node_support_pin = cJSON_GetObjectItem(root, "support_pin");
    node_enable_pin = cJSON_GetObjectItem(root, "enable_pin");

    if (node_support_pin != NULL && node_enable_pin != NULL)
    {
        ret = node_support_pin->type == cJSON_True && node_enable_pin->type == cJSON_True;
    }
    else
    {
        ret = 0;
    }

END:
    if (root)
    {
        cJSON_Delete(root);
    }

    if (info_str_json)
    {
        master_heap_free(info_str_json);
    }

    return ret;
}

int masterPINVerify(MASTER_HANDLE hD2CHandle, char *pPIN, int nPinLen)
{
	// 开发锁启用PIN码检查，当启用PIN码时执行PIN码验证操作，否则返回成功。
	// 旧版本开发锁（控制锁，固件版本低于3.1.20）不支持PIN码
    if (masterEnablePIN(hD2CHandle))
    {
        return master_pin_verify(hD2CHandle, PIN_DEFAULT_INDEX, (SS_BYTE*)pPIN, nPinLen);
    }

    return SS_OK;
}

int masterPINDeauth(MASTER_HANDLE hD2CHandle)
{
    master_pin_deauth(hD2CHandle);
    return SS_OK;
}
